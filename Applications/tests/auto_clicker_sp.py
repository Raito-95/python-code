def find_color_matches(screen, unique, tolerance=20):
    data = np.array(screen)
    screen_colors = data.reshape(-1, data.shape[2])
    differences = np.abs(screen_colors[:, np.newaxis] - unique)
    matches = np.any(np.all(differences <= tolerance, axis=2), axis=1)
    # Calculate the percentage of matches
    match_percentage = np.mean(matches) * 100
    return match_percentage


def color_printing_thread(stop_event, interval=1):
    while not stop_event.is_set():
        x, y = pyautogui.position()
        left = max(0, int(x - RANGE_BOX))
        top = max(0, int(y - RANGE_BOX))
        right = min(pyautogui.size().width, int(x + 1 + RANGE_BOX))
        bottom = min(pyautogui.size().height, int(y + 1 + RANGE_BOX))

        screen = ImageGrab.grab(bbox=(left, top, right, bottom))
        match_percentage = find_color_matches(screen, unique_colors, TOLERANCE)
        print(f'Matching percentage: {match_percentage}%')
        print("---\n")
        time.sleep(interval)


def main():
    clicking_enabled = False
    stop_event = threading.Event()
    thread = threading.Thread(target=color_printing_thread, args=(stop_event,))
    thread.start()

    try:
        while True:
            if keyboard.is_pressed('z'):
                clicking_enabled = not clicking_enabled
                print("Clicking enabled:", clicking_enabled)
                time.sleep(0.5)
            if clicking_enabled:
                x, y = pyautogui.position()
                left = int(x - RANGE_BOX)
                top = int(y - RANGE_BOX)
                right = int(x + RANGE_BOX)
                bottom = int(y + RANGE_BOX)

                screen = ImageGrab.grab(bbox=(left, top, right, bottom))
                match_percentage = find_color_matches(screen, unique_colors)
                if match_percentage >= 90.0:
                    pyautogui.click()
                    print(f"Clicked at ({x}, {y}) with {match_percentage}% match.")
                    time.sleep(0.1)
    except KeyboardInterrupt:
        print("Keyboard interrupt detected. Exiting...")
    finally:
        stop_event.set()
        thread.join()
        print("Program exited.")


if __name__ == "__main__":
    main()
